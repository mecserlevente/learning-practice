    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String input = jTextField5.getText();
        if (input.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nem adtál meg adatot!", "HIBA", JOptionPane.PLAIN_MESSAGE);
        }
        boolean vane = false;
        int hely = 0;
        for (int i = 0; i < lista.size(); i++) {
            String cime = lista.get(i).cim;
            boolean equals1 = input.equalsIgnoreCase(cime);

            if (lista.get(i).cim.equals(input) || lista.get(i).cim.contains(input) || equals1) {
                vane = true;
                hely = i;
            }
        }
        if (vane) {
            jLabel9.setText("Keresett film címe: " + lista.get(hely).cim);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sajnos nincs ilyen film a listában.", "HIBA", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

