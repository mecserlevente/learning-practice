    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText() != "" && jTextField2.getText() != "" && jTextField3.getText() != "" && jTextField4.getText() != "") {
            try {
                String cim = jTextField1.getText();
                int ev = Integer.parseInt(jTextField2.getText());
                int dij = Integer.parseInt(jTextField3.getText());
                int jelolesek = Integer.parseInt(jTextField4.getText());

                PrintWriter pr = new PrintWriter(new FileWriter("oscar.csv", true));
                int azonosito = (int) (Math.random() * 10000 + 1);
                String azon = azonosito + "";
                pr.println(azon + ";" + cim + ";" + ev + ";" + dij + ";" + jelolesek);

                pr.close();
                JOptionPane.showMessageDialog(rootPane, "Az adatok kiírása megtörtént az oscar.csv-be!", "Sikerült!", JOptionPane.PLAIN_MESSAGE);

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Nem számot adtál meg", "HIBA", JOptionPane.PLAIN_MESSAGE);
            } catch (IOException ex) {
                Logger.getLogger(neve_oscar.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Nem adtál meg minden adatot!", "HIBA", JOptionPane.PLAIN_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

