    public static ArrayList<adatok> lista = new ArrayList<>();

    public static void beolvas(ArrayList<adatok> lista) throws FileNotFoundException {
        Scanner fileinput = new Scanner(new File("oscar.csv"));
        String elsosor = fileinput.nextLine();
        while (fileinput.hasNextLine()) {
            String data = fileinput.nextLine();
            String tomb[] = data.split(";");
            lista.add(new adatok(tomb[0], tomb[1], Integer.parseInt(tomb[2]), Integer.parseInt(tomb[3]), Integer.parseInt(tomb[4])));

        }
        fileinput.close();

    }

    public void cimetkiirat(ArrayList<adatok> lista) {
        ArrayList<String> cimek = new ArrayList<>();
        for (int i = 0; i < lista.size(); i++) {
            cimek.add(lista.get(i).cim);
        }

        Collections.sort(cimek);
        for (int i = 0; i < cimek.size(); i++) {
            String asd = cimek.get(i);
            cimekhez.append(asd + "\n");
        }
    }

